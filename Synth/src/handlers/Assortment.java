package handlers;

import java.util.LinkedList;

import components.Component;

/**
 * Assortment.java
 * 
 * Basically a handler that creates components based on its location.
 * 
 * 
 *
 */
public class Assortment {
	public LinkedList<Component> list = new LinkedList<Component>();

	public Assortment() {
		// Uhhhhh... Do nothing.... Yeah...
	}

	public Assortment(Component[] initList) {
		for (int i = 0; i < initList.length; i++) {
			addComponent(initList[i]);
		}
	}
	
	public void addComponent(Component component) {
		list.add(component);
	}
}
